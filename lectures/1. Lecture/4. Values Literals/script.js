/**
 * Variable values that hardcoded in source code
 * called literals
 */

/**
 * String literals can be set using 'value'  or "value" symbols
 */
var stringLiteral = 'This is string litreal value',
    anotherStringLiteral = "This is another string literal";

/**
 * Number literals
 * For decimals you need use comma symbol
 */
var numerLiteral = 15,
    anotherNumberLiteral = 10.50;

var a = 1,     // Number literal
    b = 2.5,   // Another number literal
    c = a + b; // Not literal, just expression