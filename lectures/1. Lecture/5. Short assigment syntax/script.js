/**
 * There is posibility to use short assigment
 * such as:
 * +=
 * -=
 * *=
 * /=
 * %=
 */
var x = 10;
console.log('x', x);

x += 10; // The same as x = x + 10
console.log('x', x);

x -= 10; // The same as x = x - 10
console.log('x', x);

x *= 10; // The same as x = x * 10
console.log('x', x);

x /= 10; // The same as x = x / 10
console.log('x', x);

x %= 10; // The same as x = x % 10
console.log('x', x);
  